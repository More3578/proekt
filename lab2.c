#include <stdio.h>
#include "../include/lab2.h"

void lab2_bas() {
     float s, a, c, b, eps;
    int i, sign;
    const int MAX_ITERATIONS = 1000; // Безопасное ограничение

    // Ввод точности
    printf("eps: ");
    scanf("%f", &eps);

    // Инициализация переменных
    s = 0;           // сумма ряда
    sign = 1;        // знак элемента (1 или -1)
    i = 0;           // счетчик элементов
    c = 3;           // числитель (начинается с 3)
    b = 1;           // знаменатель (начинается с 1)

    // Вычисляем первый член ряда
    a = c / b;

    // Цикл с двойным условием: точность И ограничение шагов
    while ((a > eps || a < -eps) && i < MAX_ITERATIONS)
    {
        s = s + a * sign;     // добавляем элемент с нужным знаком
        sign = -sign;         // меняем знак на противоположный
        i = i + 1;            // увеличиваем счетчик

        // Формируем следующий член ряда
        c = c + 1; // числитель увеличивается на 1
        b = b + 3; // знаменатель увеличивается на 3
        a = c / b; // вычисляем новый член ряда
    }

    // Вывод результата
    printf("n = %d, s = %f\n", i, s);

    // Дополнительная проверка: не вышли ли мы по ограничению
    if (i == MAX_ITERATIONS) {
        printf("error.\n");
    }
}

